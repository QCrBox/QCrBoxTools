from typing import Dict, List, Optional


def generate_aliases(
    dic_files: List[str], manual_additions: Optional[Dict[str, str]] = None
) -> Dict[str, str]:  # pragma: no cover
    """
    Generates a dictionary of aliases from CIF dictionary files and manual additions.

    This function reads CIF dictionary files to extract alias information and combines
    it with manually specified additions. It ensures that aliases do not create circular
    references and removes self-references.
    This function was used on the 30.01.2024 version of available .dic files to
    generate the dictionaries below and is mainly kept here for reference and to rerun
    the analysis with a new set of dic files to update the dict used for aliases.

    Parameters
    ----------
    dic_files : List[str]
        Paths to CIF dictionary files.
    manual_additions : Optional[Dict[str, str]]
        Manually specified aliases. Defaults to common CIF entry corrections.

    Returns
    -------
    Dict[str, str]
        A dictionary mapping aliases to their canonical CIF entry names.

    Raises
    ------
    ModuleNotFoundError
        If PyCIFRW is not installed, necessary for reading CIF files.
    AssertionError
        If circular references are detected in the alias mapping.

    """

    if manual_additions is None:
        manual_additions = {
            "diffrn_refln_crystal_id": "diffrn_refln.crystal_id",
            "refln_crystal_id": "refln.crystal_id",
            "symmetry_Int_Tables_number-M": "space_group_IT_number",
        }
    try:
        from CifFile import ReadCif  # from PyCIFRW
    except ModuleNotFoundError as exc:
        raise ModuleNotFoundError("PyCifRW missing, install via pip or conda") from exc

    aliases = {}
    values = []

    for dic_file in dic_files:
        cifobj = ReadCif(str(dic_file))
        for _, frame_content in cifobj.child_table.items():
            block = cifobj[frame_content.block_id]
            valid_replacement = all(
                (
                    "_definition_replaced.by" in block,
                    isinstance(block.get("_definition_replaced.by"), str),
                    block.get("_definition_replaced.by") != ".",
                )
            )
            if valid_replacement:
                convert_to = block["_definition_replaced.by"][1:]
                aliases[frame_content.block_id] = convert_to
            else:
                convert_to = frame_content.block_id
                values.append("_" + frame_content.block_id)
            if "_alias.definition_id" not in block:
                continue
            alias_entry = block["_alias.definition_id"]
            if isinstance(alias_entry, str):
                aliases[alias_entry[1:]] = convert_to
                if alias_entry.lower() != alias_entry:
                    aliases[alias_entry.lower()[1:]] = convert_to
            else:
                for alias in alias_entry:
                    aliases[alias[1:]] = convert_to
                    if alias.lower() != alias:
                        aliases[alias.lower()[1:]] = convert_to

    for alias, target in manual_additions.items():
        aliases[alias] = aliases.get(target, target)

    # remove self references as they are pointless
    aliases = {key: val for key, val in aliases.items() if key != val}

    # ensure there are no circular translations
    assert not any(
        val in aliases for val in values
    ), "One of the common references has gotten an alias, creating a circle"

    return aliases


